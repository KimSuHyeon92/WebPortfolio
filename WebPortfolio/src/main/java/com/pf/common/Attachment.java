package com.pf.common;

import java.util.Date;

import org.apache.ibatis.type.Alias;

@Alias("Attachment")
public class Attachment {

	private int attachSeq;
	private String attachDocType;	// 게시판 종류
	private int attachDocKey;	// 게시글의 pk
	private String filename;		// 실제 파일 이름
	private String fakeName;		// 물리 디스크에 저장할 이름
	private Long fileSize;			// 파일 크기
	private String contentType;		// 파일 종류
	private Date createDate;		// 생성일
	
	public int getAttachSeq() {
		return attachSeq;
	}
	public void setAttachSeq(int attachSeq) {
		this.attachSeq = attachSeq;
	}
	public String getAttachDocType() {
		return attachDocType;
	}
	public void setAttachDocType(String attachDocType) {
		this.attachDocType = attachDocType;
	}
	public int getAttachDocKey() {
		return attachDocKey;
	}
	public void setAttachDocKey(int attachDocKey) {
		this.attachDocKey = attachDocKey;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getFakeName() {
		return fakeName;
	}
	public void setFakeName(String fakeName) {
		this.fakeName = fakeName;
	}
	public Long getFileSize() {
		return fileSize;
	}
	public void setFileSize(Long fileSize) {
		this.fileSize = fileSize;
	}
	public String getContentType() {
		return contentType;
	}
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
}
